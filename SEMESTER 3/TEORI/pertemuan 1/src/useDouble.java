public class useDouble {
    public static void main(String[] args) {
        double angka1 = 10.5;
        double angka2 = 5.2;

        System.out.println("Angka 1: " + angka1);
        System.out.println("Angka 2: " + angka2);

        double hasilTambah = angka1 + angka2;
        double hasilKurang = angka1 - angka2;
        double hasilKali = angka1 * angka2;
        double hasilBagi = angka1 / angka2;

        System.out.println("Hasil Penjumlahan: " + hasilTambah);
        System.out.println("Hasil Pengurangan: " + hasilKurang);
        System.out.println("Hasil Perkalian: " + hasilKali);
        System.out.println("Hasil Pembagian: " + hasilBagi);
    }
}
